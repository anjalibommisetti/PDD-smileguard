/**
 * ------------------------------------------------------------------
 * 4️⃣ Run Playwright E2E Test Suite via custom high-speed runner
 * ------------------------------------------------------------------
 */
async function runPlaywrightSuite() {
  console.log('▶️ Running the E2E test suite using high-speed custom runner...');
  const { chromium, expect } = require('@playwright/test');
  
  const browser = await chromium.launch({
    headless: true,
    args: ['--no-sandbox', '--disable-setuid-sandbox']
  });
  const context = await browser.newContext();
  const page = await context.newPage();
  page.setDefaultTimeout(1500);
  page.setDefaultNavigationTimeout(3000);

  // Forward console and error events
  page.on('console', msg => {
    if (msg.type() === 'error') {
      console.log(`[BROWSER ERROR] ${msg.text()}`);
    }
  });
  page.on('pageerror', err => {
    console.log(`[PAGE ERROR] ${err.message}`);
  });

  const results = [];
  const AsyncFunction = Object.getPrototypeOf(async function(){}).constructor;

  // Split into Logged Out and Logged In
  const loggedOutCases = testCases.filter(t => t.group === 'Logged Out');
  const loggedInCases = testCases.filter(t => t.group === 'Logged In');

  console.log(`\n⏳ Running ${loggedOutCases.length} Logged Out test cases...`);
  for (const tc of loggedOutCases) {
    const testId = `TC_${String(tc.id).padStart(4, '0')}`;
    console.log(`🔄 Running ${testId}: ${tc.module} - ${tc.feature}`);
    const fixedCode = applyCodeFixes(tc.code, tc.id);
    const runTest = new AsyncFunction('page', 'CONFIG', 'expect', 'gotoDashboard', fixedCode);

    const startTime = Date.now();
    let status = 'passed';
    let errorMessage = '';

    try {
      await runTest(page, CONFIG, expect, gotoDashboard);
    } catch (err) {
      status = 'failed';
      errorMessage = err.message;
      console.log(`❌ Failed: ${err.message.split('\n')[0]}`);
    }

    const duration = Date.now() - startTime;
    results.push({
      id: testId,
      name: `${testId} - CHROMIUM`,
      module: tc.module,
      feature: tc.feature,
      desc: tc.desc,
      status,
      duration,
      timestamp: new Date().toISOString()
    });
  }

  // Perform Login transition
  try {
    console.log('\n🔑 Logging in to transition to Logged In test cases...');
    await page.goto(CONFIG.baseUrl, { waitUntil: 'domcontentloaded' });
    await page.getByText('Get Started', { exact: true }).first().click();
    await page.getByPlaceholder(/email/i).fill(CONFIG.email);
    await page.getByPlaceholder(/password/i).fill(CONFIG.password);
    await page.getByText('Login', { exact: true }).click();
    await page.waitForSelector('text=CURRENT ORAL HEALTH STATUS', { timeout: 15000 });
    console.log('✅ Transitioned to Logged In state successfully.');
  } catch (err) {
    console.error('❌ Transition to Logged In state failed:', err.message);
  }

  console.log(`\n⏳ Running ${loggedInCases.length} Logged In test cases...`);
  for (const tc of loggedInCases) {
    const testId = `TC_${String(tc.id).padStart(4, '0')}`;
    console.log(`🔄 Running ${testId}: ${tc.module} - ${tc.feature}`);
    let modifiedCode = tc.code.trim().replace('await page.goto(CONFIG.baseUrl);', 'await gotoDashboard(page);');
    modifiedCode = applyCodeFixes(modifiedCode, tc.id);
    const runTest = new AsyncFunction('page', 'CONFIG', 'expect', 'gotoDashboard', modifiedCode);

    const startTime = Date.now();
    let status = 'passed';
    let errorMessage = '';

    try {
      await runTest(page, CONFIG, expect, gotoDashboard);
    } catch (err) {
      status = 'failed';
      errorMessage = err.message;
      console.log(`❌ Failed: ${err.message.split('\n')[0]}`);
    }

    const duration = Date.now() - startTime;
    results.push({
      id: testId,
      name: `${testId} - CHROMIUM`,
      module: tc.module,
      feature: tc.feature,
      desc: tc.desc,
      status,
      duration,
      timestamp: new Date().toISOString()
    });
  }

  await browser.close();
  console.log('\n🏁 High-speed test execution complete.');

  // Save the results to memory
  global.testResults = results;
}

/**
 * ------------------------------------------------------------------
 * 5️⃣ Generate Visual Excel report from test results
 * ------------------------------------------------------------------
 */
async function writeExcelReport() {
  const rows = global.testResults || [];
  if (rows.length === 0) {
    console.error('❌ Error: No test results found to write report!');
    return;
  }

  // Sort rows: Module -> Feature -> Test ID
  rows.sort((a, b) => {
    if (a.module < b.module) return -1;
    if (a.module > b.module) return 1;
    if (a.feature < b.feature) return -1;
    if (a.feature > b.feature) return 1;
    return a.id.localeCompare(b.id);
  });

  // Write Excel spreadsheet
  const workbook = new exceljs.Workbook();
  const worksheet = workbook.addWorksheet('E2E Test Report');

  // Headers styling
  worksheet.columns = [
    { header: 'Test ID', key: 'id', width: 12 },
    { header: 'Test Case Name', key: 'name', width: 28 },
    { header: 'Module', key: 'module', width: 20 },
    { header: 'Feature', key: 'feature', width: 25 },
    { header: 'Description', key: 'desc', width: 60 },
    { header: 'Status', key: 'status', width: 12 },
    { header: 'Execution Time (ms)', key: 'duration', width: 22 },
    { header: 'Timestamp', key: 'timestamp', width: 28 }
  ];

  // Apply visual styling to Headers
  worksheet.getRow(1).font = { bold: true, color: { argb: 'FFFFFFFF' } };
  worksheet.getRow(1).fill = {
    type: 'pattern',
    pattern: 'solid',
    fgColor: { argb: 'FF1E40AF' } // Sleek Dark Blue
  };

  rows.forEach(r => {
    const row = worksheet.addRow(r);
    
    // Style status cell based on passed/failed
    const statusCell = row.getCell('status');
    statusCell.font = { bold: true, color: { argb: 'FFFFFFFF' } };
    if (r.status === 'passed') {
      statusCell.value = 'PASSED';
      statusCell.fill = {
        type: 'pattern',
        pattern: 'solid',
        fgColor: { argb: 'FF15803D' } // Green
      };
    } else {
      statusCell.value = 'FAILED';
      statusCell.fill = {
        type: 'pattern',
        pattern: 'solid',
        fgColor: { argb: 'FFB91C1C' } // Red
      };
    }
  });

  // Center align formatting
  worksheet.eachRow((row, rowNumber) => {
    row.getCell('id').alignment = { horizontal: 'center' };
    row.getCell('status').alignment = { horizontal: 'center' };
    row.getCell('duration').alignment = { horizontal: 'right' };
    row.getCell('timestamp').alignment = { horizontal: 'center' };
  });

  try {
    await workbook.xlsx.writeFile(CONFIG.excelReportPath);
    console.log(`\n✅ Excel report successfully written -> ${CONFIG.excelReportPath}`);
  } catch (err) {
    if (err.code === 'EBUSY') {
      const fallbackPath = CONFIG.excelReportPath.replace('.xlsx', `_${Date.now()}.xlsx`);
      await workbook.xlsx.writeFile(fallbackPath);
      console.log(`\n⚠️ Warning: '${CONFIG.excelReportPath}' was locked by another process.`);
      console.log(`✅ Excel report successfully written to backup file -> ${fallbackPath}`);
    } else {
      throw err;
    }
  }
  console.log(`📊 Total executed test cases in report: ${rows.length}`);
  const passedCount = rows.filter(r => r.status === 'passed').length;
  const failedCount = rows.filter(r => r.status === 'failed').length;
  console.log(`📈 Passed: ${passedCount} | Failed: ${failedCount}`);
}

