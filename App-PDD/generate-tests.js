  console.log('🔎 Initializing test generation orchestration...');
  
  // 1. Generate the 305 functional E2E tests
  generateTestFile();

  // 2. Execute the tests
  await runPlaywrightSuite();

  // 3. Extract results and output sorted Excel sheet
