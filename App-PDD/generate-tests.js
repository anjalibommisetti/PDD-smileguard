/**
 * ------------------------------------------------------------------
 * 1️⃣ One-time setup to log in and save the session state (auth.json)
 * ------------------------------------------------------------------
 */
async function prepareAuthState() {
  console.log('🔑 Performing one-time login setup to save auth state...');
  const { chromium } = require('@playwright/test');
  const browser = await chromium.launch({ headless: true });
  const context = await browser.newContext();
  const page = await context.newPage();

  try {
    await page.goto(CONFIG.baseUrl, { waitUntil: 'domcontentloaded' });
    await page.getByText('Get Started', { exact: true }).first().click();
    await page.getByPlaceholder(/email/i).fill(CONFIG.email);
    await page.getByPlaceholder(/password/i).fill(CONFIG.password);
    await page.getByText('Login', { exact: true }).click();
    
    // Wait for Dashboard to show up
    await page.waitForSelector('text=CURRENT ORAL HEALTH STATUS', { timeout: 15000 });

    const authPath = path.resolve(__dirname, 'auth.json');
    await context.storageState({ path: authPath });
    console.log(`✅ Auth state saved successfully to ${authPath}`);
  } catch (err) {
    console.error('❌ Failed to prepare auth state:', err.message);
  } finally {
    await browser.close();
  }
}

